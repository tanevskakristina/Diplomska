// Get current language from localStorage or default to Macedonian
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'mk';
}

// Set language
function setLanguage(lang) {
    if (lang === 'en' || lang === 'mk') {
        localStorage.setItem('language', lang);
        location.reload();
    }
}

// Get translation text
function t(key) {
    const lang = getCurrentLanguage();
    return translations[lang][key] || key;
}

// Translate all elements with data-translate attribute
function translatePage() {
    const lang = getCurrentLanguage();
    
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.textContent = translations[lang][key] || key;
    });

    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        element.placeholder = translations[lang][key] || key;
    });

    document.querySelectorAll('[data-translate-value]').forEach(element => {
        const key = element.getAttribute('data-translate-value');
        element.value = translations[lang][key] || key;
    });

    // Update language switcher
    const langSwitcher = document.getElementById('langSwitcher');
    if (langSwitcher) {
        langSwitcher.textContent = lang === 'en' ? 'MK' : 'EN';
    }
}

// Initialize translation on page load
document.addEventListener('DOMContentLoaded', () => {
    translatePage();
});
